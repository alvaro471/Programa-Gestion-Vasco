    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        String nombreArchivo = "merged";

        if (nombreArchivo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debes ingresar un nombre para el archivo PDF.");
            return;
        }

        // Obtener la ruta inicial desde ManejadorArchivos
        String rutaInicial = ManejadorArchivos.getRutaCarpeta();
        JFileChooser selectorCarpeta = new JFileChooser(rutaInicial != null ? new File(rutaInicial) : null);
        selectorCarpeta.setDialogTitle("Seleccione la carpeta donde desea guardar el PDF fusionado");
        selectorCarpeta.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        selectorCarpeta.setAcceptAllFileFilterUsed(false);

        int seleccion = selectorCarpeta.showSaveDialog(this);

        if (seleccion != JFileChooser.APPROVE_OPTION) {
            // Usuario canceló la selección
            return;
        }

        File carpetaDestino = selectorCarpeta.getSelectedFile();

        if (!carpetaDestino.exists() || !carpetaDestino.isDirectory()) {
            JOptionPane.showMessageDialog(this, "La carpeta destino no existe o no es válida.");
            return;
        }

        // ✅ Sincronizar la lista real antes de fusionar
        ManejadorArchivos.limpiarRutasArchivos();

        for (int i = 0; i < modeloLista1Rutas.size(); i++) {
            ManejadorArchivos.agregarRutaArchivo(modeloLista1Rutas.get(i));
        }

        try {
            ManejadorArchivos.unirPDFs(nombreArchivo, carpetaDestino);
            JOptionPane.showMessageDialog(this, "Archivo PDF combinado creado correctamente.");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Error al crear el PDF: " + ex.getMessage());
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton7ActionPerformed

