    private void btnBajarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBajarActionPerformed
        int[] indices = JListConsolidado.getSelectedIndices();

        if (indices.length == 0 || indices[indices.length - 1] >= modeloLista1Nombres.size() - 1) return;

        for (int i = indices.length - 1; i >= 0; i--) {
            int index = indices[i];
            if (index < modeloLista1Nombres.size() - 1 && !contains(indices, index + 1)) {
                // Intercambiar en modelo visual (nombres)
                String nombre = modeloLista1Nombres.get(index);
                modeloLista1Nombres.set(index, modeloLista1Nombres.get(index + 1));
                modeloLista1Nombres.set(index + 1, nombre);

                // Intercambiar en modelo lógico (rutas)
                String ruta = modeloLista1Rutas.get(index);
                modeloLista1Rutas.set(index, modeloLista1Rutas.get(index + 1));
                modeloLista1Rutas.set(index + 1, ruta);

                indices[i]++; // Actualizar índice seleccionado
            }
        }

        JListConsolidado.setSelectedIndices(indices);
    }//GEN-LAST:event_btnBajarActionPerformed

